-- ========== 1. CRÉATION DES TABLES ==========

-- 1. Table Utilisateurs (Liée à auth.users de Supabase)
CREATE TABLE IF NOT EXISTS public.utilisateurs (
    id UUID PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
    nom TEXT NOT NULL,
    email TEXT UNIQUE NOT NULL,
    telephone TEXT,
    role TEXT NOT NULL CHECK (role IN ('admin', 'visiteur')) DEFAULT 'visiteur',
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Fonction de Trigger pour insérer automatiquement dans 'utilisateurs' lors d'un signup
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS trigger AS $$
BEGIN
  INSERT INTO public.utilisateurs (id, nom, email, telephone, role)
  VALUES (
    new.id,
    COALESCE(new.raw_user_meta_data->>'nom', split_part(new.email, '@', 1)),
    new.email,
    COALESCE(new.raw_user_meta_data->>'telephone', ''),
    'visiteur'
  );
  RETURN new;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Exécution du trigger sur la table raw auth.users
DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;
CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE PROCEDURE public.handle_new_user();

-- 2. Table Artistes & Réalisateurs
CREATE TABLE IF NOT EXISTS public.artistes_realisateurs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nom TEXT NOT NULL,
    biographie TEXT,
    photo_url TEXT,
    pays TEXT,
    type TEXT CHECK (type IN ('Humoriste', 'Réalisateur', 'Autre', 'Direction FESTICO', 'Coordinateur', 'Candidate Miss', 'Candidat Film', 'Candidat Humour', 'Refusé')),
    email TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 3. Table Événements
CREATE TABLE IF NOT EXISTS public.evenements (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    titre TEXT NOT NULL,
    description TEXT,
    artiste_id BIGINT REFERENCES public.artistes_realisateurs(id) ON DELETE SET NULL,
    type TEXT CHECK (type IN ('Film', 'Humour', 'Atelier', 'Autre')),
    date_heure TIMESTAMP WITH TIME ZONE NOT NULL,
    lieu TEXT NOT NULL,
    prix_fcfa INTEGER NOT NULL DEFAULT 0,
    places_totales INTEGER NOT NULL,
    places_restantes INTEGER NOT NULL,
    image_url TEXT,
    est_publie BOOLEAN DEFAULT false,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 4. Table Commandes
CREATE TABLE IF NOT EXISTS public.commandes (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    utilisateur_id UUID REFERENCES public.utilisateurs(id) ON DELETE CASCADE,
    montant_total INTEGER NOT NULL,
    statut TEXT CHECK (statut IN ('en_attente', 'confirmee', 'annulee')) DEFAULT 'en_attente',
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 5. Table Paiements
CREATE TABLE IF NOT EXISTS public.paiements (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    commande_id UUID UNIQUE REFERENCES public.commandes(id) ON DELETE CASCADE,
    methode TEXT CHECK (methode IN ('MTN Mobile Money', 'Orange Money', 'Carte', 'Autre')),
    reference_operateur TEXT UNIQUE,
    statut TEXT CHECK (statut IN ('en_attente', 'reussi', 'echoue')) DEFAULT 'en_attente',
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 6. Table Billets
CREATE TABLE IF NOT EXISTS public.billets (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    commande_id UUID REFERENCES public.commandes(id) ON DELETE CASCADE,
    evenement_id BIGINT REFERENCES public.evenements(id) ON DELETE CASCADE,
    code_qr TEXT UNIQUE NOT NULL,
    statut_scan BOOLEAN DEFAULT false,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);



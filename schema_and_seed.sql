-- ========== 3. CONFIGURATION RLS (Row Level Security) ==========
-- Pour que l'application frontend puisse lire les données
ALTER TABLE public.evenements ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.artistes_realisateurs ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.utilisateurs ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.commandes ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.billets ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.paiements ENABLE ROW LEVEL SECURITY;

-- Autoriser la lecture publique de l'équipe et des events
CREATE POLICY "Lecture publique événements" ON public.evenements FOR SELECT USING (true);
CREATE POLICY "Lecture publique artistes" ON public.artistes_realisateurs FOR SELECT USING (true);

-- Utilisateurs: l'utilisateur modifie/lit sa propre ligne
CREATE POLICY "Lecture propre utilisateur" ON public.utilisateurs FOR SELECT USING (auth.uid() = id);
CREATE POLICY "Insert propre utilisateur" ON public.utilisateurs FOR INSERT WITH CHECK (auth.uid() = id);
-- Fonction permettant aux admins de lire toute la table (évite l'infinite recursion RLS)
CREATE OR REPLACE FUNCTION public.is_admin()
RETURNS BOOLEAN AS $$
DECLARE
  v_role TEXT;
BEGIN
  SELECT role INTO v_role FROM public.utilisateurs WHERE id = auth.uid();
  RETURN v_role = 'admin';
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE POLICY "Lecture de tous si admin" ON public.utilisateurs FOR SELECT USING ( public.is_admin() );

-- Commandes, Billets, Paiements : insertion/lecture pour l'user
CREATE POLICY "Commandes auth.uid" ON public.commandes FOR ALL USING (auth.uid() = utilisateur_id);
CREATE POLICY "Admin All Commandes" ON public.commandes FOR ALL USING ( public.is_admin() );

CREATE POLICY "Paiements All" ON public.paiements FOR ALL USING (true);
CREATE POLICY "Billets All" ON public.billets FOR ALL USING (true);

